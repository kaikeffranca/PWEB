var n1 = parseFloat(prompt("Digite o primeiro número: "));
var n2 = parseFloat(prompt("Digite o segundo número: "));

alert("Soma: " + (n1+n2).toFixed(2));
alert("Subtração: "+ (n1-n2).toFixed(2));
alert("Multiplicação: "+ (n1*n2).toFixed(2));
alert("Divisão: "+ (n1/n2).toFixed(2));
alert("Resto da divisão: "+ (n1%n2).toFixed(2));